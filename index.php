    <script>
        document.querySelector('form').addEventListener('submit', function(event) {
            event.preventDefault();
            const coffeeType = document.getElementById('coffeeType').value;
            const quantity = parseInt(document.getElementById('quantity').value);
            const price = calculatePrice(coffeeType, quantity);
            document.getElementById('orderSummary').innerText = `You ordered ${quantity} ${coffeeType}(s). Total: $${price.toFixed(2)}`;
        });
        
        function calculatePrice(coffeeType, quantity) {
            const prices = {
                espresso: 2.50,
                cappuccino: 3.00,
                latte: 3.50,
                americano: 2.00
            };
            return prices[coffeeType] * quantity;
        }
